import numpy as np

k = np.array([
    [0.4486620098814102,  0.20476430802289014],
    [0.3458375648947594, 0.1603773057669117]
])

tau = np.array(
    [103.0, 82.0, 90.0, 42.0]
)

F1 = 250
F2 = 325


def G(s): 
    G = np.array([
        [
            k[0, 0]/(tau[0]*s + 1),
            k[0, 1]/((tau[1]*s + 1)*(tau[3]*s + 1))
        ],
        [
            k[1, 0]/((tau[0]*s + 1)*(tau[2]*s + 1)),
            k[1, 1]/(tau[1]*s + 1)
        ]
    ])
    return G

print(G(0.5)@[F1, F2])